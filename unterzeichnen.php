<p>
#FRIDAYSFORFUTURE SIND DRINGENDER WECKRUF – WIRTSCHAFT WILL ENDLICH MEHR KLIMASCHUTZ UMSETZEN</p>
<p>Inzwischen streiken und demonstrieren weltweit junge Menschen für ambitionierten Klimaschutz, für das Benennen der Klimakrise als echte Krise und für die Rettung unseres Planeten. Die historische Mobilisierungskraft der Bewegung zeigt die Dringlichkeit des Anliegens. Auch die Wissenschaft hat mit tausenden Unterzeichnenden der Initiative #Scientists4Future.org erklärt, dass die derzeitigen Maßnahmen zum Klima-, Arten-, Wald-, Boden- und Meeresschutz bei weitem nicht ausreichen. Als Unternehmerinnen und Unternehmer erklären wir uns mit den Streikenden solidarisch.</p>

<p>Aus unserem eigenen tagtäglichen unternehmerischen Handeln wissen wir: Klimaschonendes Wirtschaften funktioniert heute schon. <b><em>Viele innovative Technologien, Produkte, Dienstleistungen und Geschäftsmodelle für einen besseren Klimaschutz gibt es bereits. Aber weitergehende politische Rahmenbedingungen sind endlich nötig, damit faire Wettbewerbsbedingungen für klimaschonende Technologien und Geschäftsmodelle hergestellt werden und die Wirtschaft insgesamt umsteuert.</em></b></p>

<p>Die Staaten der Weltgemeinschaft haben sich mit dem Pariser Klimaschutzabkommen von 2015 völkerrechtlich verbindlich dazu verpflichtet, die globale Erwärmung deutlich unter 2°C zu halten. Alle Länder haben außerdem Anstrengungen versprochen, die Erwärmung auf 1,5°C zu begrenzen.</p>

<p>Als unterzeichnende Unternehmerinnen und Unternehmer stehen wir hinter dem Pariser Klima­schutz­abkommen und unterstützen diese Forderungen:</p>
<ol>
    <li><b><em>Eine wirksame und planbar steigende CO2-Bepreisung für alle Sektoren.</em></b> Investitionen werden so in eine kohlenstoffarme Infrastruktur, Technologien und Produkte gelenkt.</li>
    <li><b><em>Energiewende beschleunigen.</em></b> Schneller, gesetzlich verankerter Ausstieg aus den fossilen Energien. Österreich, Europa und die Welt können technisch zu 100% aus erneuerbaren Energien versorgt werden. Eine umfassende Energieeffizienzstrategie zur Verankerung von Efficiency First in allen Bereichen, insbesondere Förderungen für Gebäudesanierungen sowie ambitionierte gesetzliche Vorgaben mit Vorbildrolle der öffentlichen Hand.</li>
    <li><b><em>Divestment und Abschaffung klimaschädlicher Subventionen</em></b> bis zum Jahr 2025, wie z.B. von den G7-Staaten beschlossen.</li>
    <li><b><em>Eine Mobilitätswende</em></b>, die auf Verkehrsvermeidung, öffentlichen Verkehr, umweltfreundliche und geteilte Verkehrsmittel sowie C02-freie Antriebssysteme setzt. Wir brauchen die Besteuerung des Flugverkehrs (Kerosinsteuer) und reduzierte Umsatzsteuern auf Produkte und Dienstleistungen des Radverkehrs.</li>
    <li><b><em>Eine Agrar- und Ernährungswende.</em></b> Die Subventionspolitik in der Landwirtschaft und die Rahmenbedingungen für die Ernährungswirtschaft müssen in Einklang mit den Klimaschutzzielen stehen – und mit wirksamen Maßnahmen wie der Ausdehnung einer ökologischen und klimaschonenden Landwirtschaft und der Förderung entsprechender Ernährungs- und Konsumstile unterstützt werden.</li>
    <li><b><em>Kreislaufwirtschaft forcieren.</em></b> In der Regel ist die Nutzung von Sekundärrohstoffen der Nutzung von Primärrohstoffen ökologisch überlegen, da sie mit weniger Energieaufwand hergestellt werden können und auf geringere Transportwege angewiesen sind. Rücknahme-, Recycling-, und insbesondere die Wiederverwertungsquoten müssen nicht nur gesetzt, sondern auch durchgesetzt werden.</li>
    <li><b><em>Aufbau eines Klima-Innovationsfonds für etablierte Unternehmen und Startups</em></b>, die innovative Lösungen für die Steuerung/ Bewältigung der Klimakrise realisieren.
    </li><b><em>Ein ambitioniertes Klimaschutzgesetz</em></b>, das eine dekarbonisierte Wirtschaft im Einklang mit dem in Paris vereinbarten 1,5°C Ziel erreicht.</li>
    <li><b><em>Ein nachhaltiges Finanzsystem.</em></b> Die von der EU-Kommission für Europa geschätzten Kosten von 180 Mrd. Euro jährlich zur Erreichung der Klima- und Energieziele können aufgebracht werden. Dazu bedarf es auch eines nachvollziehbaren Labels für nachhaltige Investitionen sowie gesetzlicher Regelungen – sei es durch steuerliche Anreize oder Vorschriften über die Mittelverwendung -, um privaten und institutionellen Investoren einen Rahmen zu bieten, ihren Beitrag bei der Mittelaufbringung zu leisten.</li>
</ol>

<p>Wir setzen voller Überzeugung auf Klimaschutz!</p>
<p>Als Profis aus der Wirtschaft wissen wir um die Kraft innovativer Geschäftsmodelle und Technologien, die das Klima schützen und profitabel sind. Die konsequente und schnelle Einführung erneuerbarer Energien, einer ökologischen Landwirtschaft, einer Kreislauf­wirtschaft und einer neuen Mobilität schaffen und sichern zukunftsfähige Arbeitsplätze. Klimaschützende Technologien und Geschäftsmodelle scheitern häufig an politischen Rahmenbedingungen, die Geschäftsmodelle und Technologien der Vergangenheit schützen. Die vergangenen 20 Jahre haben gezeigt, dass die technologische Entwicklung und Möglichkeiten für den Klimaschutz rasant gestiegen und die Kosten immens gesunken sind.</p>
<p>Um die gesamte Wirtschaft zu transformieren, benötigen wir dringend andere politische Rahmenbedingungen.</p>
<p>Wenn wir jetzt handeln, ist eine geregelte Dekarbonisierung der Wirtschaft möglich. Nur dann schaffen wir eine enkeltaugliche Wirtschaft. Nur dann können wir die Klimakrise noch steuern und unsere natürlichen Lebensgrundlagen erhalten. <b><em>Dafür streiken die Kinder und Jugendlichen von „Fridays for Future“; ihnen gebührt unsere Achtung und unsere volle Unterstützung.</em></b></p>
 
<p>#EntrepreneursForFuture</p>

